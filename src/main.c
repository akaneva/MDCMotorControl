/**
 * @brief Configures the system clock for the STM32F407 to run at 168MHz using a 12MHz external crystal.
 * 
 * @note This function sets up the PLL and bus clocks for optimal performance.
 * No input parameters; modifies global clock settings. Calls Error_Handler() on failure.
 */
void SystemClock_Config(void) {
    RCC_OscInitTypeDef RCC_OscInitStruct = {0};
    RCC_ClkInitTypeDef RCC_ClkInitStruct = {0};

    // 1. Enable Power Control clock
    __HAL_RCC_PWR_CLK_ENABLE();
    __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

    // 2. Configure HSE (High Speed External oscillator) and PLL
    // Olimex E407 has a 12MHz crystal. We multiply it to reach 168MHz.
    RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE;
    RCC_OscInitStruct.HSEState = RCC_HSE_ON;
    RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
    RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
    RCC_OscInitStruct.PLL.PLLM = 12;  // Divide 12MHz by 12 = 1MHz
    RCC_OscInitStruct.PLL.PLLN = 336; // Multiply 1MHz by 336 = 336MHz
    RCC_OscInitStruct.PLL.PLLP = RCC_PLLP_DIV2; // Divide 336MHz by 2 = 168MHz CPU Clock
    RCC_OscInitStruct.PLL.PLLQ = 7;   // USB Clock
    
    if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK) {
        Error_Handler();
    }

    // 3. Configure CPU, AHB, and APB buses clocks
    RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK | RCC_CLOCKTYPE_SYSCLK
                                | RCC_CLOCKTYPE_PCLK1 | RCC_CLOCKTYPE_PCLK2;
    RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
    RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;       // 168 MHz
    RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV4;        // 42 MHz (Timers on APB1 will run at 84 MHz)
    RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV2;        // 84 MHz (Timers on APB2 will run at 168 MHz)

    if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_5) != HAL_OK) {
        Error_Handler();
    }
}
